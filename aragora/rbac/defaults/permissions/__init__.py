"""
RBAC Permission Definitions.

Contains all system-wide permissions organized by resource type.

This package provides backward-compatible access to all permissions that were
previously defined in the monolithic permissions.py file. Permissions are now
organized into domain-specific modules:

- admin: Admin, system, metrics, queue, scheduler, backup/DR, features
- billing: Billing, finance, cost, payments, quotas, receipts
- compliance: Compliance, audit, policy, data governance, PII, findings
- debate: Debate, agent, workflow, control plane, gauntlet, orchestration
- integrations: Connectors, webhooks, gateway, API keys, vendors, plugins
- knowledge: Knowledge, memory, reasoning, provenance, analytics, RLM
- organization: Users, org, teams, workspaces, roles, auth, sessions
- resources: Documents, feedback, devices, marketplace, skills, inbox, etc.

Usage:
    # Import individual permissions
    from aragora.rbac.defaults.permissions import PERM_DEBATE_CREATE, PERM_ADMIN_ALL

    # Import from specific modules
    from aragora.rbac.defaults.permissions.debate import PERM_DEBATE_CREATE
    from aragora.rbac.defaults.permissions.admin import PERM_ADMIN_ALL
"""

